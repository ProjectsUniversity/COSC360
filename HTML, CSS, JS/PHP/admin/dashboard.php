// Fetch dashboard statistics
$stats = [
    'users' => $pdo->query("SELECT COUNT(*) as count FROM users")->fetch(PDO::FETCH_ASSOC)['count'],
    'employers' => $pdo->query("SELECT COUNT(*) as count FROM employers")->fetch(PDO::FETCH_ASSOC)['count'],
    'jobs' => $pdo->query("SELECT COUNT(*) as count FROM jobs WHERE status = 'active'")->fetch(PDO::FETCH_ASSOC)['count'],
    'applications' => $pdo->query("SELECT COUNT(*) as count FROM applications")->fetch(PDO::FETCH_ASSOC)['count']
];

// Fetch recent activity
$recentActivity = $pdo->query("
    SELECT * FROM (
        SELECT 'user' as type, full_name as name, created_at as date 
        FROM users 
        ORDER BY created_at DESC 
        LIMIT 5
    ) as user_activity
    UNION ALL
    SELECT * FROM (
        SELECT 'employer' as type, company_name as name, created_at as date 
        FROM employers 
        ORDER BY created_at DESC 
        LIMIT 5
    ) as employer_activity
    ORDER BY date DESC 
    LIMIT 10
")->fetchAll(PDO::FETCH_ASSOC);
